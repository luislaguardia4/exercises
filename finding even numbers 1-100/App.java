public class App {
    public static void main(String[] args) throws Exception {
      
        for (int i = 2; i <= 100; i += 2) {
            System.out.println(i);
        }

//In this program, we start the loop from 2 (the first even number) 
//and increment by 2 in each iteration, effectively generating
//all even numbers between 1 and 100. The loop continues until the value of i is
//less than or equal to 100. The System.out.println(i) statement inside the loop prints 
//each even number on a new line.

    }
}
